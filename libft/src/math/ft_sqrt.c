#include "libft.h"

int	ft_sqrt(int x)
{
	int	i;
	int	res;

	i = 1;
	res = 0;
	while (res < x)
	{
		res = i * i;
		if (res == x)
			return (i);
		i++;
	}
	return (0);
}
