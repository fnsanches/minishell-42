#include "libft.h"

int	ft_cntdigit(int n)
{
	int			i;
	long long	nbr;

	i = 1;
	nbr = n;
	if (n < 0)
		nbr = nbr * -1;
	while (n >= 10)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
